document.addEventListener('DOMContentLoaded', function () {
    const tabButtons = document.querySelectorAll('.tab-btn');
    const tabContents = document.querySelectorAll('.tab-content');

    tabButtons.forEach(button => {
        button.addEventListener('click', () => {
            const tabName = button.getAttribute('data-tab');

            // Update tab button styles
            tabButtons.forEach(btn => {
                btn.classList.remove('text-indigo-500', 'font-semibold', 'border-b-2', 'border-indigo-500');
                btn.classList.add('text-gray-500', 'hover:text-gray-700');
            });
            button.classList.remove('text-gray-500', 'hover:text-gray-700');
            button.classList.add('text-indigo-500', 'font-semibold', 'border-b-2', 'border-indigo-500');

            // Show the selected tab content
            tabContents.forEach(content => {
                content.classList.add('hidden');
            });
            document.getElementById(`${tabName}-table`).classList.remove('hidden');
        });
    });
});
                   






